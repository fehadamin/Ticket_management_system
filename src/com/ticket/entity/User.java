package com.ticket.entity;

public class User {

	private int userId;
	private String password;
	private String name;
	private String userName;
	private String email;
	private String role;
	private int departmentId;
	private String homeCompany;
	private String created;
	/* setter and getter methods for userId*/
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	/* setter and getter methods for password*/
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	/* setter and getter methods for name*/
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	/* setter and getter methods for userName*/
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/* setter and getter methods for email*/
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	/* setter and getter methods for role*/
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	/* setter and getter methods for departmentId*/
	public int getDepartmentId() {
		return departmentId;
	}
	public void setDepartmentId(int departmentId) {
		this.departmentId = departmentId;
	}
	/* setter and getter methods for homeCompany*/
	public String getHomeCompany() {
		return homeCompany;
	}
	public void setHomeCompany(String homeCompany) {
		this.homeCompany = homeCompany;
	}
	/* setter and getter methods for created*/
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	

}
	
	


